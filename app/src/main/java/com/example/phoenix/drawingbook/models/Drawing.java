public class Drawing {
    private String topBar;
    private String botBar;
    private String topDash;
    private String midDash;
    private String botDash;

    public String getTopBar() {
        return topBar;
    }

    public void setTopBar(String topBar) {
        this.topBar = topBar;
    }

    public String getBotBar() {
        return botBar;
    }

    public void setBotBar(String botBar) {
        this.botBar = botBar;
    }

    public String getTopDash() {
        return topDash;
    }

    public void setTopDash(String topDash) {
        this.topDash = topDash;
    }

    public String getMidDash() {
        return midDash;
    }

    public void setMidDash(String midDash) {
        this.midDash = midDash;
    }

    public String getBotDash() {
        return botDash;
    }

    public void setBotDash(String botDash) {
        this.botDash = botDash;
    }

}
